#verificar se os numeros sao iguais e qual é a soma deles
n1 =int(input("informe se o primeiro é: "))
n2 =int(input("informe se o segundo é: "))
if n1 == n2:
    print(f"os valores sao iguais, entao o produto é {n1*n2}")
else:
    print(f"os valores sao diferentes, entao a soma é {n1+n2}")
    
